export const TRADING_ROLE_BY_ID = Object.fromEntries(
  TRADING_ROLES.map((r) => [r.id, r]),
) as Record<TradingAgentId, TradingRoleProfile>

/** Map backend agent_id to frontend id (news_analyst -> news_policy_analyst). */
export function normalizeAgentId(id: string | null | undefined): TradingAgentId | null {
  if (!id) return null
  if (id === 'news_analyst') return 'news_policy_analyst'
  if (id in TRADING_ROLE_BY_ID) return id as TradingAgentId
  return null
}

