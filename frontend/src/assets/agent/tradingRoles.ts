function role(profile: TradingRoleProfile): TradingRoleProfile {
  return profile
}

function pos(id: TradingAgentId) {
  const p = AGENT_POSITIONS[id]
  return {
    position: p.home,
    deskPosition: p.home,
    meetingPosition: p.meeting ?? p.debate ?? p.home,
  }
}

